function App() {
  const [prompt, setPrompt] = useState('')
  const [previewHtml, setPreviewHtml] = useState(starterPreview)
  const [status, setStatus] = useState('Ready')
  const [isGenerating, setIsGenerating] = useState(false)
  const [error, setError] = useState('')
  const [now] = useState(() => Date.now())
  const [recent, setRecent] = useState(() => {
    try {
      return JSON.parse(localStorage.getItem('karthiable-recent')) || []
    } catch {
      return []
    }
  })

  useEffect(() => {
    localStorage.setItem('karthiable-recent', JSON.stringify(recent.slice(0, 6)))
  }, [recent])

  const canDownload = useMemo(() => previewHtml.trim().startsWith('<!DOCTYPE html>'), [previewHtml])
  const displayedRecent = recent.length ? recent : [
    { id: 'sample-1', title: 'Modern SaaS Landing Page', prompt: examples[1], html: starterPreview, createdAt: now - 120000 },
    { id: 'sample-2', title: 'AI Dashboard UI', prompt: examples[0], html: starterPreview, createdAt: now - 900000 },
    { id: 'sample-3', title: 'E-commerce Store', prompt: examples[2], html: starterPreview, createdAt: now - 3600000 },
  ]

  async function handleGenerate(event) {
    event.preventDefault()
    const trimmedPrompt = prompt.trim()
    if (!trimmedPrompt) {
      setError('Describe what you want to build first.')
      return
    }

    setIsGenerating(true)
    setStatus('Generating')
    setError('')

    try {
      const response = await fetch(`${API_URL}/generate`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ prompt: trimmedPrompt }),
      })

      if (!response.ok) {
        throw new Error(`Backend returned ${response.status}`)
      }

      const html = await response.text()
      setPreviewHtml(html)
      setStatus('Ready')
      setRecent((items) => [
        {
          id: crypto.randomUUID(),
          title: trimmedPrompt.length > 34 ? `${trimmedPrompt.slice(0, 34)}...` : trimmedPrompt,
          prompt: trimmedPrompt,
          html,
          createdAt: Date.now(),
        },
        ...items,
      ].slice(0, 6))
    } catch (err) {
      setStatus('Offline')
      setError(`Could not reach the generator at ${API_URL}. Start the backend and try again.`)
      console.error(err)
    } finally {
      setIsGenerating(false)
    }
  }

  function handleDownload() {
    const blob = new Blob([previewHtml], { type: 'text/html' })
    const url = URL.createObjectURL(blob)
    const link = document.createElement('a')
    link.href = url
    link.download = 'karthiable-generated.html'
    link.click()
    URL.revokeObjectURL(url)
  }

  function loadRecent(item) {
    setPrompt(item.prompt)
    setPreviewHtml(item.html)
    setStatus('Ready')
    setError('')
  }

  return (
    <div className="app-shell">
      <aside className="sidebar">
        <div className="brand">
          <div className="brand-mark">K</div>
          <span>KARTHIABLE</span>
        </div>

        <nav className="nav-list" aria-label="Main navigation">
          {sidebarItems.map((item, index) => (
            <button className={index === 0 ? 'active' : ''} type="button" key={item}>
              <span className="nav-icon"><Icon name={index === 0 ? 'grid' : 'spark'} /></span>
              {item}
            </button>
          ))}
        </nav>

        <div className="upgrade-card">
          <strong>Upgrade to Pro</strong>
          <p>Unlimited Generations</p>
          <p>Priority Access</p>
          <p>Export and Deploy</p>
          <button type="button">Upgrade Now</button>
        </div>

        <div className="profile-card">
          <div className="avatar">K</div>
          <div>
            <strong>Karthi</strong>
            <span>Pro Plan</span>
          </div>
        </div>
      </aside>

      <main className="main-panel">
        <header className="topbar">
          <label className="search-box">
            <Icon name="search" />
            <input type="search" placeholder="Search anything..." />
            <kbd>Ctrl K</kbd>
          </label>

          <div className="top-actions">
            <button type="button" className="icon-button" aria-label="Theme toggle">o</button>
            <button type="button" className="icon-button" aria-label="Notifications">!</button>
            <button
              className="new-project"
              type="button"
              onClick={() => {
                setPrompt('')
                setPreviewHtml(starterPreview)
                setStatus('Ready')
                setError('')
              }}
            >
              New Project <Icon name="plus" />
            </button>
          </div>
        </header>

        <section className="hero-section">
          <div className="orbital-glow" />
          <p className="eyebrow">Describe it. Generate it. Launch it.</p>
          <h1>KARTHIABLE</h1>
          <p className="hero-copy">
            The AI-powered web app generator that turns your ideas into beautiful,
            functional websites in seconds.
          </p>
        </section>

        <form className="prompt-card" onSubmit={handleGenerate}>
          <label htmlFor="prompt">What do you want to build?</label>
          <textarea
            id="prompt"
            value={prompt}
            onChange={(event) => setPrompt(event.target.value)}
            placeholder="E.g. A futuristic dashboard for analytics..."
          />

          <div className="prompt-actions">
            <div className="chips">
              {examples.map((example) => (
                <button key={example} type="button" onClick={() => setPrompt(example)}>
                  {example.includes('landing') ? 'Landing Page' : example.includes('store') ? 'Storefront' : 'Web App'}
                </button>
              ))}
            </div>
            <div className="generate-actions">
              <button type="button" className="ghost-button" onClick={() => setPrompt((value) => `${value} Make it premium, responsive, and animated.`.trim())}>
                Enhance
              </button>
              <button className="primary-button" type="submit" disabled={isGenerating}>
                {isGenerating ? 'Generating...' : 'Generate'}
              </button>
            </div>
          </div>
          {error && <p className="error-message">{error}</p>}
        </form>

        <section className="inline-preview-card" aria-label="Generated website preview">
          <div className="preview-heading">
            <div>
              <span className={`status ${status.toLowerCase()}`}>{status}</span>
              <h2>Generated Page Preview</h2>
            </div>
            <button type="button" onClick={handleDownload} disabled={!canDownload}>
              Download HTML
            </button>
          </div>
          <div className="large-browser-frame">
            {isGenerating && (
              <div className="preview-loader">
                <span />
                Building your website preview...
              </div>
            )}
            <iframe title="Generated website full preview" srcDoc={previewHtml} sandbox="allow-scripts" />
          </div>
        </section>

        <section className="feature-grid">
          {featureCards.map((feature) => (
            <article className="feature-card" key={feature.title}>
              <div className="feature-icon"><Icon name={feature.icon} /></div>
              <h2>{feature.title}</h2>
              <p>{feature.text}</p>
            </article>
          ))}
        </section>

        <section className="cta-card">
          <div className="mini-cube" />
          <div>
            <h2>Your ideas. Our AI. Limitless possibilities.</h2>
            <p>Start building the future with Karthiable.</p>
          </div>
          <button type="button" onClick={handleGenerate} disabled={isGenerating || !prompt.trim()}>
            Get Started Now
          </button>
        </section>
      </main>

      <aside className="preview-panel">
        <section className="preview-card">
          <div className="section-title">
            <h2>Mini Preview</h2>
            <span className={`status ${status.toLowerCase()}`}>{status}</span>
            <button type="button" onClick={handleDownload} disabled={!canDownload} aria-label="Download generated HTML">
              <Icon name="external" />
            </button>
          </div>
          <div className="browser-frame">
            <iframe title="Generated website preview" srcDoc={previewHtml} sandbox="allow-scripts" />
          </div>
        </section>

        <section className="recent-card">
          <div className="section-title">
            <h2>Recent Generations</h2>
            <button type="button" onClick={() => setRecent([])}>Clear</button>
          </div>

          <div className="recent-list">
            {displayedRecent.map((item) => (
              <button className="recent-item" type="button" key={item.id} onClick={() => loadRecent(item)}>
                <span className="thumb" />
                <span>
                  <strong>{item.title}</strong>
                  <small>{timeAgo(item.createdAt, now)}</small>
                </span>
                <i>...</i>
              </button>
            ))}
          </div>
        </section>
      </aside>
    </div>
  )
}

export default App
