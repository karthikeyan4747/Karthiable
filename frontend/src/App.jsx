const sidebarItems = ['Dashboard', 'Generate', 'My Projects', 'Templates', 'Chat History', 'Favorites', 'Settings']

const examples = [
  'A futuristic dashboard for analytics with neon charts',
  'A modern SaaS landing page for an AI coding assistant',
  'An e-commerce store homepage for premium sneakers',
]

function timeAgo(timestamp, now) {
  const seconds = Math.max(1, Math.floor((now - timestamp) / 1000))
  if (seconds < 60) return 'just now'
  const minutes = Math.floor(seconds / 60)
  if (minutes < 60) return `${minutes} min${minutes > 1 ? 's' : ''} ago`
  const hours = Math.floor(minutes / 60)
  if (hours < 24) return `${hours} hour${hours > 1 ? 's' : ''} ago`
  const days = Math.floor(hours / 24)
  return `${days} day${days > 1 ? 's' : ''} ago`
}

